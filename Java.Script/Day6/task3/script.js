var p = document.getElementById('PAR');

function ChangeFont (font){
    p.style.fontFamily = font;
}


function ChangeAlign (align) {
    p.style.textAlign = align;
}

function ChangeHeight (height) {
    p.style.lineHeight = height;
}

function ChangeLSpace (spacing) {
    p.style.letterSpacing = spacing;
}

function ChangeIndent (indent) {
    p.style.textIndent = indent;
}

function ChangeTransform (trans) {
    p.style.textTransform = trans;
}

function ChangeDecorate (decor) {
    p.style.textDecoration = decor;
}

function ChangeBorder (border) {
    p.style.border = border;
}

function ChangeBorderColor (bColor) {
    p.style.borderColor = bColor;
}